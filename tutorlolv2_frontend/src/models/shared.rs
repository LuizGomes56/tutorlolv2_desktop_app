macro_rules! impl_unsafe_cast {
    ($t:ty, $n:ty) => {
        impl $t {
            pub fn unsafe_cast(n: $n) -> $t {
                unsafe { std::mem::transmute(n) }
            }
        }
    };
}

impl_unsafe_cast!(ChampionId, u8);
impl_unsafe_cast!(ItemId, u16);
impl_unsafe_cast!(RuneId, u8);

impl Into<usize> for ItemId {
    fn into(self) -> usize {
        self as usize
    }
}

impl Into<usize> for RuneId {
    fn into(self) -> usize {
        self as usize
    }
}

pub trait UnsafeCast: Sized {
    fn unsafe_cast_usize(n: usize) -> Self;
}

impl UnsafeCast for RuneId {
    #[inline]
    fn unsafe_cast_usize(n: usize) -> Self {
        unsafe { std::mem::transmute(n as u8) }
    }
}

impl UnsafeCast for ItemId {
    #[inline]
    fn unsafe_cast_usize(n: usize) -> Self {
        unsafe { std::mem::transmute(n as u16) }
    }
}

